        lai3:flip()
        wait_twn(lai3:movetoX(1300))
        lai3:say("两位大爷，这酒香亭虽小，可也是江湖中人的聚集地。两位要是能在这喝个开心，那也是给我们酒香亭长脸了。")
        haozhu1:say("你这家伙，嘴还挺甜。今儿个就给你这个面子，以后这酒香亭就是我们哥俩儿的歇脚处了，你可得好好招待。")
        lai3:say("那是当然，有两位大爷的光临，我们酒香亭的生意想不火都难。")
        haozhu2:say("你小子聪明，知道怎么做人。但要是敢在背后搞鬼，这酒香亭就不用开了。")
        lai3:say("两位放心，我赖三别的不会，伺候人的本事还是有的。只要两位大爷高兴，我这酒香亭就是两位的第二个家，绝不敢有半点差池。")
