        haozhu1:say("小子，把你们最烈的酒给爷们儿拿出来，今儿个我们哥俩儿要喝他个天翻地覆！")
        haozhu2:say("对，赶紧把酒端上来，别他娘的拿些兑水的货色来应付爷们儿！")
