    haozhu1:setPos(700, 75)
    haozhu1:face_right()
    haozhu2:setPos(1000, 75)
    haozhu2:face_right()

    wait_twn(lai3:movetoX(1300), lai3:offsetY(-50))
    if cnt == 0 then
