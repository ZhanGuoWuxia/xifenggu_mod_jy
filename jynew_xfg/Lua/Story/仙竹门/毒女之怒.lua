shaoyao:say("不要啊！掌门饶命啊！那里面都是剧毒蜘蛛！掌门！不要！")
nangongyan:say("阿拉索，你去虎焰门，带的人由你挑，帮我带两样东西回来。")
nangongyan:say("一个是姑获鸟之瓶，一个是(player:fullname)的人头。")
alasuo:say("是！")
hide_all_stage_bubble()

alasuo:movetoX(2500)
wait_twn(shaoyao:movetoX(2500))

local diplomatic = require("Common/diplomatic")
diplomatic.declare_war("仙竹门", "player")

black_scene(0.25)
hide_stage()
set_flag("完成毒女之怒")
light_scene(0.25)
