hangong:say("赵左使此言何意？")
zhaorang:say("倘若真按韩右使此番安排，置香儿于何境地？")
hangong:say("东方家的老二东方豚对大小姐情有独钟，必会全心全意呵护大小姐。更何况龙湾门的龙夫人与我曾有交情，她定会玉成此事。")
