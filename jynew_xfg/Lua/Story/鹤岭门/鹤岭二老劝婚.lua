zhaorang:say("但愿如此，不然别怪我为了爱徒，不讲同僚情面！")
hangong:say("这门婚事本来就是为大小姐觅得佳婿，更何况又有你这个师父照拂，还有什么不放心的？")
zhaorang:say("如此倒也在理。")

